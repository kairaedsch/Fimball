package sep.fimball.model.blueprint.base;

/**
 * BaseElementCategory ist eine Auflistung von Kategorien, in die die BaseElements eingeteilt werden.
 */
public enum BaseElementCategory
{
    /**
     * Elemente dieser Kategorie gehören zu den Basis-Elementen.
     */
    BASIC,

    /**
     * Elemente dieser Kategorie gehören zu den Spezial-Elementen.
     */
    ADVANCED,

    /**
     * Elemente dieser Kategorie gehören zu den Hindernis-Elementen.
     */
    OBSTACLE,

    /**
     * Elemente dieser Kategorie gehören zu dne Rampen-Elementen.
     */
    RAMP
}
